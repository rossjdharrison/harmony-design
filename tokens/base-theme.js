/**
 * Deep merge utility for theme composition
 * @param {Object} target - Base object
 * @param {Object} source - Override object
 * @returns {Object} Merged object
 */
export function deepMerge(target, source) {
  const result = { ...target };
  
  for (const key in source) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      result[key] = deepMerge(target[key] || {}, source[key]);
    } else {
      result[key] = source[key];
    }
  }
  
  return result;
}

/**
 * Create a theme by composing base theme with overrides
 * @param {Object} overrides - Brand-specific token overrides
 * @returns {Object} Composed theme
 */
export function composeTheme(overrides = {}) {
  return deepMerge(baseTheme, overrides);
}